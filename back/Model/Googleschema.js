const mongoose = require("mongoose");

const dataschema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
  },
  contact: {
    type: String,
    required: true,
  },
  password: {
    type: String,
    required: true,
  },
});

const Google = mongoose.model("Google", dataschema);
module.exports = Google;
