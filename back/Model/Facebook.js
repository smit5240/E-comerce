const mongoose = require("mongoose");

const dataschema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
  },
  contact: {
    type: String,
    required: true,
  },
  password: {
    type: String,
    required: true,
  },
  gender: {
    type: String,
    required: true,
  },
});

const facebook = mongoose.model("facebook", dataschema);
module.exports = facebook;
